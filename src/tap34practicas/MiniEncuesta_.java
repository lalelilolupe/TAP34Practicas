    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String sistema=" ";
        
        if(jRadioButton1.isSelected())
            sistema = sistema + jRadioButton1.getText();
        else{
            if(jRadioButton2.isSelected())
                sistema = sistema + jRadioButton2.getText();
            else{
                if(jRadioButton3.isSelected())
                    sistema = sistema + jRadioButton3.getText();
            }
        }
        
        String especialidad = " ";
        
        if(jCheckBox1.isSelected())
            especialidad = especialidad + jCheckBox1.getText();
        if(jCheckBox2.isSelected())
            especialidad = especialidad + " " + jCheckBox2.getText();
        if(jCheckBox3.isSelected())
            especialidad = especialidad + " " + jCheckBox3.getText();
        
        int x = jSlider1.getValue();
        jLabel4.setText(""+x);
        
        JOptionPane.showMessageDialog(this, "Tu sistema operativo preferido es: "+sistema+"\nTu especialidad/es es: "+especialidad+"\nHoras dedicadas al ordenador: "+x, "Muestra de datos", JOptionPane.INFORMATION_MESSAGE, null);
    }//GEN-LAST:event_jButton1ActionPerformed

